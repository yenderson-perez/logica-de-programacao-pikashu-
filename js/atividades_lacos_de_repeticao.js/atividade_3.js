//3. **Tabuada**
//Desenvolva um programa que mostre a tabuada do número 5.
// Considere que a tabuada vai do `0 x 5` ao `10 x`
 let numero = 5;
 let i;
 let resultado;
  
 for( i=1; i<=10; i++ ){
      resultado = i * numero;
      console.log( i + "*" + numero + "=" + resultado)
 }
 console.log(`🐢`)